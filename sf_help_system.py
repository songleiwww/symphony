TRIGGER_WORDS = [
    '帮助', 'help', '使用说明', '怎么用', '如何使用', 
    '功能', '介绍', '指南', '教程', '说明',
    '求助', '帮忙', 'assist'
]

def is_help_request(message: str) -> bool:
    """判断是否是帮助请求"""
    msg = message.lower()
    for word in TRIGGER_WORDS:
        if word in msg:
            return True
    return False

